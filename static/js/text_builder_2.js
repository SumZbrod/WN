function setupTextAudio() {

    document.body.appendChild(audioPlayer);
    
    // –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Ç–∞–π–º–ª–∞–π–Ω–∞
    currentVoiceAudio.addEventListener('timeupdate', () => {
        const progressPercent = (currentVoiceAudio.currentTime / currentVoiceAudio.duration) * 100;
        progress.style.width = progressPercent + '%';
        const mins = Math.floor(currentVoiceAudio.currentTime / 60);
        const secs = Math.floor(currentVoiceAudio.currentTime % 60);
        currentTime.textContent = `${mins}:${secs < 10 ? '0' : ''}${secs}`;
    });
    // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–±—ã—Ç–∏—è –Ω–∞–∂–∞—Ç–∏—è –ø—Ä–æ–±–µ–ª–∞
    document.addEventListener('keydown', (event) => {
        // console.log(event.code)
        if (event.code === 'Space') {
            event.preventDefault(); // –ü—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –ø–æ–≤–µ–¥–µ–Ω–∏–µ –ø—Ä–æ–±–µ–ª–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, –ø—Ä–æ–∫—Ä—É—Ç–∫–∞)
            addNextBlock();
            textContainer.scrollTo(0, textContainer.scrollHeight+100); // –ü—Ä–æ–∫—Ä—É—á–∏–≤–∞–µ–º –∫ –Ω–∞—á–∞–ª—É
        }
        else if (event.code === 'KeyR') {
            currentVoiceAudio.pause();
            currentVoiceAudio.currentTime = 0;
            currentVoiceAudio.play().catch(error => {
                console.error('–û—à–∏–±–∫–∞ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è –∞—É–¥–∏–æ:', error);
            });
        }
    });
    
    // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —Å –ø–µ—Ä–≤–æ–π –ø–∞—á–∫–∏
    fetchTextBatch((batch) => {
        currentBatch = batch;
        // addNextBlock();
    });
}

